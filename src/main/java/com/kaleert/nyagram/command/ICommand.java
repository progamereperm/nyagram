package com.kaleert.nyagram.command;

/**
 * Базовый интерфейс для реализации команд бота (устаревший подход).
 * <p>
 * В современных версиях Nyagram рекомендуется использовать аннотации {@link CommandHandler}
 * и {@link BotCommand}, однако этот интерфейс может быть полезен для динамической регистрации
 * или сложной кастомной логики.
 * </p>
 */
public interface ICommand {
    
    /**
     * Выполняет основную логику команды.
     * @param context Контекст, предоставляющий доступ к Update и Raw API.
     */
    void execute(CommandContext context);

    /**
     * Определяет, может ли данная команда обработать текущий Update.
     * @param context Входящий контекст с апдейтом.
     * @return true, если команда должна быть выполнена.
     */
    boolean canHandle(CommandContext context);

    /**
     * Возвращает строковое представление команды (например, "/start").
     * Может использоваться для генерации меню помощи или логирования.
     * @return Команда со слэшем или null/пустая строка, если это обработчик общего назначения.
     */
    default String getCommand() {
        return "";
    }
}
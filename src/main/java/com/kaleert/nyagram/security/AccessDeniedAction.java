package com.kaleert.nyagram.security;

/**
 * Определяет реакцию бота на попытку несанкционированного доступа.
 * Используется в {@link LevelRequired}.
 *
 * @since 1.0.0
 */
public enum AccessDeniedAction {
    /**
     * Игнорировать запрос молча. Пользователь не узнает, что команда существует.
     */
    SILENT,
    
    /**
     * Отправить стандартное уведомление "Недостаточно прав".
     */
    NOTIFY,
    
    /**
     * Записать предупреждение в лог, но пользователю ничего не отвечать.
     */
    LOG_WARNING,
    
    /**
     * Забанить пользователя (требует дополнительной настройки логики бана).
     * В базовой версии работает как NOTIFY + LOG.
     */
    BAN
}